def main():
    # Read the top 6000 words
    with open("top_6000_words.txt", "r", encoding="utf-8") as f:
        words = [line.strip() for line in f if line.strip()]

    # Read existing vocabulary
    existing = set()
    if os.path.exists("existing_vocab.txt"):
        with open("existing_vocab.txt", "r", encoding="utf-8") as f:
            for line in f:
                line = line.strip()
                if line and not line.startswith("#"):
                    # Extract Korean word from various formats
                    parts = line.split("/")
                    for part in parts:
                        part = part.strip()
                        if part and any("\uac00" <= c <= "\ud7a3" for c in part):
                            korean_word = "".join(
                                c for c in part if "\uac00" <= c <= "\ud7a3" or c in " "
                            )
                            korean_word = (
                                korean_word.split()[0]
                                if korean_word.split()
                                else korean_word
                            )
                            if korean_word:
                                existing.add(korean_word.strip())

    print(f"Loaded {len(existing)} existing vocabulary words")

    # Categorize words
    categorized = {}
    skipped = 0
    already_exists = 0

    for word in words:
        if should_skip_word(word):
            skipped += 1
            continue

        if word in existing:
            already_exists += 1
            continue

        category = categorize_word(word)
        if category not in categorized:
            categorized[category] = []
        categorized[category].append(word)

    print(f"\nProcessed {len(words)} words:")
    print(f"  - Skipped (particles/conjugations/too simple): {skipped}")
    print(f"  - Already exists: {already_exists}")
    print(f"  - New words to add: {sum(len(v) for v in categorized.values())}")
    print(f"\nCategories:")
    for cat, words_list in sorted(categorized.items()):
        print(f"  - {cat}: {len(words_list)} words")

    # Write categorized words to files
    template = """## 단어 #card
?begin
### 뜻
-
### 예
-
?end
"""

    for category, words_list in categorized.items():
        output_file = f"new_vocab_{category}.txt"
        with open(output_file, "w", encoding="utf-8") as f:
            for word in words_list:
                f.write(f"## {word} #card\n")
                f.write("?begin\n")
                f.write("### 뜻\n")
                f.write("-\n")
                f.write("### 예\n")
                f.write("-\n")
                f.write("?end\n\n")

        print(f"\nWrote {len(words_list)} words to {output_file}")


