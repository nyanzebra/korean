def should_skip_word(word):
    """Check if word should be skipped"""
    if word in SKIP_WORDS or word in TOO_SIMPLE:
        return True

    # Skip single character words (mostly particles)
    if len(word) == 1:
        return True

    # Skip words that end in typical conjugation patterns without base meaning
    conjugation_endings = [
        "는데",
        "는지",
        "잖아",
        "거든",
        "거야",
        "거예요",
        "걸요",
        "다고",
        "다는",
        "다면",
    ]
    for ending in conjugation_endings:
        if word.endswith(ending) and len(word) <= len(ending) + 2:
            return True

    return False


